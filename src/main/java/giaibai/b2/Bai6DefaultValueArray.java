package giaibai.b2;

public class Bai6DefaultValueArray {
    public static void main(String[] args) {
        int[] numbers = new int[5];

        System.out.println("Giá trị mặc định của mảng số nguyên:");

        int i = 0;
        while (i < numbers.length) {
            System.out.println(numbers[i]);
            i++;
        }
    }

}
