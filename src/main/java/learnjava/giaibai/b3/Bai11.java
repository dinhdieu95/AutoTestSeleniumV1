package learnjava.giaibai.b3;

public class Bai11 {
    public static void main(String[] args) {

        int number = 2;

        System.out.println("Các số chẵn từ 2 đến 100:");

        do {

            System.out.println(number);

            number += 2;

        } while (number <= 100);

    }
}
